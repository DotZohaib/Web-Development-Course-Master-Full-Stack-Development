//  Relational Operators
//  ==, !=, >, <, >=, <=

console.log(10 == 10);
console.log(10 === 10);
console.log(10 != 10);
console.log(10 > 10);
console.log(10 < 10);
console.log(10 >= 10);
console.log(10 <= 10);


//  Logical Operators
//  &&, ||

console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);


//  Ternary Operators
//  condition ? true : false

console.log(10 == 10 ? "Yes" : "No");
console.log(10 != 10 ? "Yes" : "No");
console.log(10 > 10 ? "Yes" : "No");
console.log(10 < 10 ? "Yes" : "No");
console.log(10 >= 10 ? "Yes" : "No");
console.log(10 <= 10 ? "Yes" : "No");

